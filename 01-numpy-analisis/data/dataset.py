    "import numpy as np\n",
    "\n",
    "# Reproducibilidad\n",
    "np.random.seed(42)\n",
    "\n",
    "# Dimensiones: (estaciones, días, horas, variables)\n",
    "n_estaciones, n_dias, n_horas, n_vars = 5, 30, 24, 6\n",
    "\n",
    "# Matriz de datos: [temp, humedad, presion, viento, precipitacion, radiacion]\n",
    "datos = np.zeros((n_estaciones, n_dias, n_horas, n_vars))\n",
    "\n",
    "# -------------------------\n",
    "# 0) Temperatura (°C)\n",
    "# -------------------------\n",
    "\n",
    "# Temperatura base por estación (5 estaciones)\n",
    "temp_base = np.array([8, 12, 14, 10, 6]).reshape(n_estaciones, 1, 1)\n",
    "\n",
    "# Horas del día (0..23) con forma para broadcast\n",
    "hora = np.arange(n_horas).reshape(1, 1, n_horas)\n",
    "\n",
    "# Variación diaria (mínimo aprox. madrugada, máximo tarde)\n",
    "variacion_diaria = 5 * np.sin((hora - 6) * np.pi / 12)\n",
    "\n",
    "# Temperatura = base + variación diaria + ruido\n",
    "datos[:, :, :, 0] = temp_base + variacion_diaria + np.random.normal(\n",
    "    loc=0, scale=2, size=(n_estaciones, n_dias, n_horas)\n",
    ")\n",
    "\n",
    "# -------------------------\n",
    "# 1) Humedad relativa (%)\n",
    "# -------------------------\n",
    "datos[:, :, :, 1] = np.clip(\n",
    "    70 - datos[:, :, :, 0] + np.random.normal(0, 10, (n_estaciones, n_dias, n_horas)),\n",
    "    30,\n",
    "    100\n",
    ")\n",
    "\n",
    "# -------------------------\n",
    "# 2) Presión (hPa)\n",
    "# -------------------------\n",
    "datos[:, :, :, 2] = 1013 + np.random.normal(0, 5, (n_estaciones, n_dias, n_horas))\n",
    "\n",
    "# -------------------------\n",
    "# 3) Viento (km/h)\n",
    "# -------------------------\n",
    "datos[:, :, :, 3] = np.abs(np.random.normal(15, 10, (n_estaciones, n_dias, n_horas)))\n",
    "\n",
    "# -------------------------\n",
    "# 4) Precipitación (mm)\n",
    "# -------------------------\n",
    "datos[:, :, :, 4] = np.maximum(0, np.random.exponential(2, (n_estaciones, n_dias, n_horas)))\n",
    "\n",
    "# -------------------------\n",
    "# 5) Radiación (W/m²)\n",
    "# -------------------------\n",
    "radiacion_base = np.maximum(0, 400 * np.sin((hora - 6) * np.pi / 12))\n",
    "datos[:, :, :, 5] = np.clip(\n",
    "    radiacion_base + np.random.normal(0, 50, (n_estaciones, n_dias, n_horas)),\n",
    "    0,\n",
    "    800\n",
    ")\n"
